<H2><U>Introduction</U> </H2>

<P>These ranking system is similar to the IFSC-World Cup,
but the <B><U>strength of the field</U></B> is taken into account by a
factor between 1 (best possible field) and 0 - called <B><U>field-factor</U></B>.
</P>

<P>The WR is calculated (since 1999) as the <u><b>sum of all results of the last 12 month</u></b>.</p>

<P>There was a minor change of the WR system starting in the
year 1999.<br>
<i>All changes are documented here in italics:<br>
Before 1999 the WR was sum of the <U>best 7 results</U> out of the <U>last
10 events</U>.
</i></P>

<H2>1. <U>field-factor</U> </H2>

<P>The reason for a field factor is clear: the weight of
a competition is chosen by the competitors who come to the competition
and not by a commission on a green table a year before. </P>

<P>The WR itself is used to get the factor (on the date
one day before the competition). Adding the points (using the a new table,
later called <A HREF="#cuwr-table">WR-table</A>, which is the regular
IFSC-table plus 15 points for every place, e.g. 115 for the first) of every
competitor who got a place in the competition leads to a sum. </P>

<P>In case of ties we add the points of the different places
and divide them by the number of ties, e.g. two 2. places: (80+15 + 65+15)
/ 2 = 87.5 for both. </P>

<P>Dividing this sum by the sum of the points of all places
we got a factor between 0 and 1. </P>

<P>So a masters-competition with only 10 invited climbers
will have a field factor less than a regular worldcup. </P>

<P>So all competitors who are under the first 30 of the actual
WR count for the field-factor not only the ones who gets points for this
competition (the first 30). </P>

<P>The field-factor is rounded to two digits behind the point
before the calculation of the individual points for the climbers.<BR>
</P>

<H2><B>2. <U>mode of the WR</U></B>
</H2>

<P>The WR is calculated since 1999 as the sum of <u><b>all</b></u> (counting) competitions of the <b><u>last 12 month</u></b> (exactly 1 year back).</p>

<i>
<P><b>Before 1999</b><br>
The <U>last 10 competitions</U> are used, because
it is nearly one year but it doesn't have the problems of a ranking which
substitutes one competition by the 'same one' this year nor the ranking
differs on the days between the competitions as it will do if it uses the
competitions of exact 1 year (e.g. the ranking of 15.01.93 will consist
of all competitions between 16.01.92 and 15.01.93).<br>
The <U>best 7 results</U> of a climber are added
for the WR.<br>
To get a larger list and to give one the opportunity to
show up in the ranking with one good placement their will be <U>no minimum
number of competitions</U> to be ranked.</P>
</i>

<H2><B>3. <U>places and points</U> </B></H2>

<P>To assign points to the competitors we use the IFSC-table
multiplying it with the field factor. </P>

<P>All points are rounded to two digits behind the point.<BR></P>

<i>
<P><b>Before 1999</b><br>
Because a 12. place in a masters with only 15 competitors
is not comparable with a 12. place in a world-cup with 60 competitors not
all climbers (if we had less then 30) can get points.<br>
A competitor has to leave at least the half field behind
him to get points. (In the above mentioned case of a competition with only
15 participants points will be assigned only to the first 7).</P>
</i>

<H2><B>4. <U>the start of the WR</U></B>
</H2>

<P>Because the WR itself is used for the calculation of
the field-factor their have to be a kind of provisional ranking before
the WR can be started: </P>

<P>The provisional ranking (for the OnSight cathegory) was
established using a fixed factor of 0.60 and the first 5 IFSC Competitions
in 1991 (4 World Cups and the World Championship).</p><p>
The speed and boulder WR's used a similar methode.<BR>
</P>

