<p><A NAME="before-1999"></A><B>(*)</b>
<i>As the above competition was before 1999, only 50% of the field get points.
<br>Nowerdays <u><b>all</b></u> competitors would get points.</i></p>

<P>The second column 'rank' is the place of the competitor
in the WR before this competition (after Black Sea '93). </P>

<P>The next column 'ex.' shows the number of ex aquos in
the WR on this place. </P>

<P>This leads with the <A HREF="#cuwr-table">WR-table</A>
to the points in the next column. All this points are added and the sum
is divided by 1268, the maximum number of points (sum of the WR-table).
</P>

<P>The last colum shows the points a competitor gets for
this competition. The field-factor read as a percentage is the same as
the points for the first place.<BR>
</P>

